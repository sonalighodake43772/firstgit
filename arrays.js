let data=[5,3,8,2,9,1];

console.log("elements:",data);
// push operation
console.log(data.push(4));
console.log("after push:",data);
// pop operation
console.log(data.pop());
console.log("after pop:",data);
// shift (deleted first  index element)
console.log(data.shift());
console.log("after shift:",data);

// unshift (add the number at first indexx)
console.log(data.unshift(0));
console.log("after unshift",data);

// indexof
console.log(data.indexOf(9));
console.log("index:",data);


